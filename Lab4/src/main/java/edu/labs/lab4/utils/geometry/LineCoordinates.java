package edu.labs.lab4.utils.geometry;

public class LineCoordinates {

  public double lineStartX;
  public double lineStartY;
  public double lineEndX;
  public double lineEndY;

  public LineCoordinates(double lineStartX, double lineStartY, double lineEndX, double lineEndY) {
    this.lineStartX = lineStartX;
    this.lineStartY = lineStartY;
    this.lineEndX = lineEndX;
    this.lineEndY = lineEndY;
  }
}